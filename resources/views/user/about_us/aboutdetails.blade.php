        <div class="details_part_two ">
            <p style="text-align: justify">Ninty eight years ago, sometime in the month of April, 1924, Shyamlal
                Sarkar,
                Bhut Nath Kar, Sashi
                Bhowmic, Amritlal Majumdar and Jyan Ranjan Roy, five of the then senior most lawyers of Calcutta,
                practicing direct taxes, joined their hands together, to form an association to protect the interest of
                the practicing lawyers, under the name of ‘INCOME TAX BAR ASSOCIATION, CALCUTTA’, in a small room within
                the Income Tax Office building at Beadon street (near Scottish Church college), Calcutta.
                <br /><br />Subsequently, in the year 1929, the Bar association office was shifted to a very small and
                stuffy
                room at
                the back stairs of the Income Tax Office building at 6, Esplanade East, Calcutta, but in the very next
                year on account of the growing membership strength, the association was allotted a comfortable room at
                Metcalfe Hall, at 1, Hare street, overlooking the Ganges. Thereafter, in the month of June, 1937, the
                Income Tax Office was shifted to 3, Government Place West, (which was known as Central Secretariat
                building) and by that time our membership strength also grew to 32 and an even more spacious room was
                required. So the association was allotted a nice and spacious ground floor room on the western block of
                the said building.
                <br /><br />The Bar association occupied this building from the year 1937 to 1962 and it is in this
                building
                that the
                activities of this Bar association grew leaps and bounds gaining in membership strength supported by a
                well stocked library.
                <br /><br />Thereafter, in the year 1962, when AYAKAR BHAWAN was built the Bar Association was allotted
                a room
                on the
                ground floor and with membership strength of 102, the office of the association shifted to the said
                building at P-7, Chowringhee square, in the month of Fenruary’62.
                <br /><br />Meanwhile, the Income Tax Appellate Tribunal, Kolkata Benches, was also functioning in
                Calcutta and
                in
                the year 1972, the Bar association was allotted a separate spacious room in the Tribunal building at ALP
                Bhawan 7th. Floor, 225/C A.J.C.Bose Road, Calcutta , where a second library of the Bar was set up.
                <br /><br />Subsequently, in the month of June, 1987, with the initiative of the then President Shri
                Chittaranjan
                Banerjee, and by the grace of the then Chief Commissioners of Income Tax Sri S.K. Gangopadhay and Dr. N.
                R. Shivaswami, the present premises of the Bar Association at AYAKAR BHAWAN (Annexe) was relocated with
                a membership strength of 374.
                <br /><br />Since then the membership continued to grow and at present the Bar Association carries a
                total
                weight of
                441 Advocates. It is worthwhile mentioning that two of our esteemed members Dr. Debi Prasad Pal and Shri
                T. D. Sugla, were elevated to the position of Hon’ble Judges of Calcutta High court and Bombay High
                court, respectively.
                <br /><br />Similarly, three of our Learned members Shri A. C. Moitra, Late A. R. Haldar and the Late S.
                L.
                Banerjee
                were appointed Hon’ble Judicial Members, of the Income Tax Appellate Tribunal. Furthermore, three of our
                members Late Beni Shankar Sharma, Late Pandit Lakshmi Kanta Maitra and Dr. Debi Prasad Pal were elected
                as Members of Parliament also.
                <br /><br />Today, the Income Tax Bar Association, Calcutta, can claim to be the oldest Income Tax Bar
                in the
                country, with legal luminaries such as Dr. Debi Prasad Pal, Shri R. N. Bajoria, Shri Nirmal Kumar
                Poddar, Shri Kalyan Roy, Late Anil Kanti Roychowdhury, Shri Nirmal Mookherjee adorning the Bar.
                <br /><br />Apart from the above, the Bar Association today has amongst its members legal stalwarts in
                the field of taxation, such as Shri Girish Narayan Singh, Shri Mahabir Prasad Thard, Shri M. L. Saraf,
                Shri Sumermal Surana, Shri K. V. Singh, Shri S. L. Kochar, Shri Meghnath Banerjee, Shri Sukhendu Prasad
                Chowdhury,
                Shri Sunil Majumdar, Shri J.P Khaitan, who always acts as a guiding force to the other members of the
                Bar.
                <br /><br />The Income Tax Bar Association Calcutta has already celebrated its Platinum jubilee in the
                year 1999
                and
                is looking forward to its centenary in the year 2024.
            </p>
