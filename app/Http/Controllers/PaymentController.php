     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        try {
            $month = implode(',', $request->input('calMonth'));
            $data = [
                'inputName' => $request->input('inputName'),
                'inputId' => $request->input('inputId'),
                'inputemail' => $request->input('inputemail'),
                'inputph' => $request->input('inputph'),
                'calMonth' => $month,
                'inputAmount' => $request->input('inputAmount'),
            ];
            //return $data;
            $payment = Payment::create($data);

            DB::commit();
            return redirect()->back()->with("Data Added Successfully");
        } catch (Exception $e) {
            DB::rollBack();
            echo $e->getMessage();
        }
    }

